// 소수상근수 - 실버1
/* 문제
양의 정수 n의 각 자리수의 제곱의 합을 계산한다.
그렇게 해서 나온 합도 각 자리수의 제곱의 합을 계산한다.
이렇게 반복해서 1이 나온다면, n을 상근수라고 한다.

700은 상근수이다.
    7^2 + 0^2 + 0^2 = 49
    4^2 + 9^2 = 97
    9^2 + 7^2 = 130
    1^2 + 3^2 + 0^2 = 10
    1^2 + 0^2 = 1

2는 상근수가 아니다.
    2^2 = 4
    4^2 = 16
    1^2 + 6^2 = 37
    3^2 + 7^2 = 58
    5^2 + 8^2 = 89
    8^2 + 9^2 = 145
    1^2 + 4^2 + 5^2 = 42
    4^2 + 2^2 = 20
    2^2 + 0^2 = 4
    4^2 = 16
    ... 끝나지 않는다

소수는 1과 자기자신을 제외하고 약수가 없는 수이다.
2, 3, 5, 7, 11, 13, 17, 19, ... 는 소수이다.

소수상근수는 소수이면서 상근수인 숫자이다.
7, 13, 19, ... 는 소수 상근수이다.

n이 주어졌을 때, n보다 작거나 같은 모든 소수상근수를 구하는 프로그램을 작성하시오.

- 입력
첫째 줄에 n (10 ≤ n ≤ 1000000)이 주어진다.
- 출력
n보다 작거나 같은 소수상근수를 한 줄에 하나씩 오름차순으로 출력한다.
*/
#include <iostream>

using namespace std;

int exp(int n) { // 각 자리수의 제곱 합
    if (n == 0) return 0;
    int digit = n % 10;
    return digit * digit + exp(n / 10);
}

bool isPrime(int x) { // 소수 판별
    if (x < 2) return false;
    for (int i = 2; i * i <= x; i++) {
        if (x % i == 0) return false;
    }
    return true;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(NULL); cout.tie(NULL);

    int n;
    cin >> n;

    for (int i = 2; i <= n; i++) { // 1은 소수가 아니므로 2부터 시작
        int num = i;

        while (num != 1 && num != 4) { // 4는 상근수가 아닌 수일 때 반복이 끝나는 수
            num = exp(num);
        }

        if (num == 1 && isPrime(i)) { // 상근수이면 소수 확인
            cout << i << "\n";
        }
    }

    return 0;
}